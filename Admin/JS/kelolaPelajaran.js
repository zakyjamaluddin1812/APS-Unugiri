

function ubah() {
    var eks = document.querySelector(".eks");
    var save = document.querySelector(".save");
    var pel = document.querySelector(".pel");
    var kel = document.querySelector(".kel");
    eks.classList.toggle("d-none")
    save.classList.toggle("d-none")
    pel.setAttribute("contenteditable", "true")
    pel.setAttribute("spellcheck", "false")
    kel.setAttribute("contenteditable", "true")
    kel.setAttribute("spellcheck", "false")
}
